#include <bits/stdc++.h>
using namespace std;
#define GL(s) getline(cin, s)
signed main() {
    cin.sync_with_stdio(false);
    cin.tie(nullptr);
    int N, P;
    cin >> N >> P;
    string aux;
    GL(aux);
    while (N--) {
        GL(aux);
    }
    cout << P << '\n';
    cout.flush();
    return 0;
}
